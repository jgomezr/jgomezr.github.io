export function spriteFor(state) {
  if (state.stage === 'egg' || !state.species) return SPRITES.egg;
  const s = SPRITES[state.species];
  if (state.stage === 'adult') return s[state.form === 'rowdy' ? 'rowdy' : 'good'];
  return s[state.stage] || s.baby;
}

export function eggSprite() { return SPRITES.egg; }

